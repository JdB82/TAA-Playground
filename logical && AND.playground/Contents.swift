import UIKit

//AND && TRUE only if both conditions are true, FALSE other wise.

var raining: Bool = false
var haveUmbrella: Bool = true

if (raining && haveUmbrella) {
    print("I don't get wet")
} else {
    print("I get wet")
}

//Second AND.

var lights: Bool = false
var electric: Bool = true

if (lights && electric) {
    print("Lights are on")
} else {
    print("It's dark here")
}

//Third AND.

var freezing: Bool = false
var iceSkates: Bool = false

if (freezing && iceSkates) {
    print("Ice skating outside")
} else {
    print("Go to the ice skate center")
}
